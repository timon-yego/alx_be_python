# Calculate simple interest

# define the variabls
principal = 1000  # Principal amount (initial investment)
rate = 0.05  # Annual interest rate (as a decimal)
time = 3     # Time the money is invested for in years

# Calculate the simple interest
I = principal * rate * time

# Print the result
print(f"The simple interest earned on an investment of {principal} at an annual interest rate of {rate*100}% for {time} years is {I}.")
