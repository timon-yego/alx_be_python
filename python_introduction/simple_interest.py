# Define the variables
principal_amount = 1000  # Principal amount (initial investment)
annual_interest_rate = 0.05  # Annual interest rate (as a decimal)
time_years = 3  # Time the money is invested for in years

# Calculate the simple interest using the formula I = P * R * T
interest_earned = principal_amount * annual_interest_rate * time_years

# Print the result
print(f"The simple interest earned on an investment of {principal_amount} at an annual interest rate of {annual_interest_rate * 100}% for {time_years} years is {interest_earned}.")
